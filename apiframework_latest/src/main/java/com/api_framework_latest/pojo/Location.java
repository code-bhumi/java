package com.api_framework_latest.pojo;

public class Location {
    
    private double Lat;
    private double Lng;


    public void setLat(double d) {
        Lat = d;
    }

    public void setLng(double d) {
        Lng = d;
    }

    public double getLat() {
        return Lat;
    }

    public double getLng() {
        return Lng;
    }
}
