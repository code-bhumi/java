public class Switch {
    public static void main(String[] args) {
        String student = "one yellow";
        switch (student) {
            case "one indigo":
               System.out.println("shivangi");
               break;
            case "one green":
               System.out.println("Atharv");  
               break;                
            case "one purple":
               System.out.println("krishna");
               break;
            case "one yellow":
               System.out.println("Adwita");
               break;
            case "one blue":
               System.out.println("pearl");
               break;
                    
        }
      
    }
}